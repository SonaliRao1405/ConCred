const now = new Date()

const isoHoursAgo = (hours) =>
  new Date(now.getTime() - hours * 60 * 60 * 1000).toISOString()

const isoDaysAgo = (days, hours = 0) =>
  new Date(now.getTime() - (days * 24 + hours) * 60 * 60 * 1000).toISOString()

